from sympy import symbols,Eq,solve
x1,x2=symbols('x1 x2')
e1 = Eq(-x1+x2,4)
e2 = Eq(-2*x1+x2,0)
resultado1=solve((e1,e2,),(x1,x2))
print (resultado1)

